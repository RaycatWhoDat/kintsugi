  IRModule, IRDecl, IRStmt, IRExpr,
  IRFuncDecl, IRVarDecl, IRExprStmt, IRIf, IRForRange, IRForEach,
  IRLoop, IRBreak, IRReturn, IRSet, IRThrow, IRTry,
